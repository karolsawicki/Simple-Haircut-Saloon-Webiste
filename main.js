// Animate Smooth Scroll
$('#view-work').on('click', function() {
  const NajlepszyFryzjerWPruszkowie = $('#NajlepszyFryzjerWPruszkowie').position().top  - 35;

  $('html, body').animate(
    {
      scrollTop: NajlepszyFryzjerWPruszkowie
    },
    1400
  );
});

